#String
x = "Hello World"
print(x)
#display the data type of x:
print(type(x))

#Integer
x = 20
print(x)
#display the data type of x:
print(type(x))

#Float
x = 20.5
print(x)
#display the data type of x:
print(type(x))

#Complex
x = 1j
print(x)
#display the data type of x:
print(type(x))

#List
x = ["apple", "banana", "cherry"]
print(x)
#display the data type of x:
print(type(x))

#tuple
x = ("apple", "banana", "cherry")
print(x)
#display the data type of x:
print(type(x))


#range
x = range(6)
print(x)
#display the data type of x:
print(type(x))


#dict
x = {"name" : "John", "age" : 36}
print(x)
#display the data type of x:
print(type(x))

#Set
x = {"apple", "banana", "cherry"}
print(x)
#display the data type of x:
print(type(x))

#frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)
#display the data type of x:
print(type(x))

#bool
x = True
print(x)
#display the data type of x:
print(type(x))

#bytes
x = b"Hello"
print(x)
#display the data type of x:
print(type(x))

#byteArray
x = bytearray(5)
print(x)
#display the data type of x:
print(type(x))

#memoryview
x = memoryview(bytes(5))
print(x)
#display the data type of x:
print(type(x))

#None
x = None
print(x)
#display the data type of x:
print(type(x))